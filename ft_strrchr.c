#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	int		ok;
	char	*a;

	i = ft_strlen(s);
	ok = 0;
	while ((i >= 0) && (ok == 0))
	{
		if (s[i] == (char)c)
		{
			ok = 1;
			a = (char *)s + i;
			return (a);
		}
		i--;
	}
	return (NULL);
}
